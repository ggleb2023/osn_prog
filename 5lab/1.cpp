/*Натуральное число из n цифр называется числом армстронга, если сумма его цифр, возведенных в n-ю степень, 
равна самому числу (например 153 = 1^3 + 5^3 + 3^3) получить все числа армстронга, состоящие из 4-х цифр*/
#include <iostream>
using namespace std;

int main(){
    
    int n = 1, k = 1;

    while (k != 9999)
    {
        
        k++; n++;
    }
    
    return 0;
}